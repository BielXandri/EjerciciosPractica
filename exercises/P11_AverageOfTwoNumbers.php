<?php

class P11_AverageOfTwoNumbers {
    public function main(): void {
        // Define two numbers
        $numA = 20;
        $numB = 15;
        $numeros = [$numA, $numB];

        // Output the formula and result
        // Write the program here
        echo "The average is " . ($numA + $numB) /count ($numeros) . "\n";
    }
}
